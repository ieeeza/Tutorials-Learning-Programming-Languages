# str.format() = optional method that giver users
#                 more control when displaying output

number = 1000

print(f'the number number is {number:.3f}')
print(f'the number number is {number:,}')
print(f'the number number is {number:b}')
print(f'the number number is {number:o}')
print(f'the number number is {number:x}')
print(f'the number number is {number:e}')